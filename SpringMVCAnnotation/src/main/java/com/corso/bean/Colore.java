package com.corso.bean;

public class Colore {
   private int R; 
   int G; 
   int B;
public Colore(int r, int g, int b) {
	super();
	R = r;
	G = g;
	B = b;
}
public int getR() {
	return R;
}
public void setR(int r) {
	R = r;
}
public int getG() {
	return G;
}
public void setG(int g) {
	G = g;
}
public int getB() {
	return B;
}
public void setB(int b) {
	B = b;
} 

}
